    INNER JOIN 
        Product p ON op.product_id = p.product_id
    INNER JOIN 
        Customer c ON o.customer_id = c.customer_id
    LEFT JOIN 
        Route r ON o.route_id = r.route_id
    WHERE 
        o.order_id = orderId;
